export interface VimeoUploadInput {
  coachId: string;
  teamId: string;
  sourceType: ExternalVideoShare['source_type'];
  sourceId: string;
  title: string;
  description?: string;
  privacySetting: PrivacySetting;
  confirmationText: string;
  /** Publicly accessible URL that Vimeo will pull the video from */
  videoUrl: string;
}

/** Maps our PrivacySetting values to Vimeo's privacy.view values */
const VIMEO_PRIVACY_MAP: Record<PrivacySetting, string> = {
  public: 'anybody',
  unlisted: 'unlisted',
  private: 'nobody',
};

/**
 * Initiate a video upload to Vimeo using the pull approach (Vimeo fetches from URL).
 * Creates an external_video_shares record, then calls the Vimeo API.
 * On failure the share record is marked 'failed' before re-throwing.
 *
 * Watermark is always applied — this is mandatory and non-configurable by callers.
 *
 * @param input - Upload parameters including coach, team, source, and Vimeo metadata
 * @returns The external_video_shares row ID for status polling
 */
export async function uploadToVimeo(input: VimeoUploadInput): Promise<string> {
  const supabase = await createClient();

  const accessToken = await getVimeoAccessToken(input.coachId);
  if (!accessToken) {
    throw new Error('Vimeo account not connected or token expired');
  }

  // Create the share record first so we have an ID to update through the lifecycle
  const { data: share, error: shareError } = await supabase
    .from('external_video_shares')
    .insert({
      coach_id: input.coachId,
      team_id: input.teamId,
      source_type: input.sourceType,
      source_id: input.sourceId,
      platform: 'vimeo',
      title: input.title,
      description: input.description ?? null,
      privacy_setting: input.privacySetting,
      watermark_applied: true,
      upload_status: 'pending',
      upload_progress: 0,
      confirmation_text: input.confirmationText,
    })
    .select('id')
    .single();

  if (shareError || !share) {
    throw new Error(`Failed to create external share record: ${shareError?.message}`);
  }

  try {
    await supabase
      .from('external_video_shares')
      .update({ upload_status: 'uploading' })
      .eq('id', share.id);

    const vimeoResponse = await fetch(`${VIMEO_API_BASE}/me/videos`, {
      method: 'POST',
      headers: {
        'Authorization': `bearer ${accessToken}`,
        'Content-Type': 'application/json',
        'Accept': VIMEO_API_ACCEPT,
      },
      body: JSON.stringify({
        upload: {
          approach: 'pull',
          link: input.videoUrl,
        },
        name: input.title,
        description: input.description ?? '',
        privacy: {
          view: VIMEO_PRIVACY_MAP[input.privacySetting],
          embed: 'public',
        },
      }),
    });

    if (!vimeoResponse.ok) {
      const errorText = await vimeoResponse.text();
      throw new Error(`Vimeo API error (${vimeoResponse.status}): ${errorText}`);
    }

    const vimeoData = await vimeoResponse.json();
    // Vimeo returns a URI like /videos/123456789
    const videoUri: string = vimeoData.uri ?? '';
    const videoId = videoUri.split('/').pop() ?? '';
    const externalUrl: string | null = vimeoData.link ?? null;

    await supabase
      .from('external_video_shares')
      .update({
        upload_status: 'processing',
        external_video_id: videoId || null,
        external_url: externalUrl,
        upload_progress: 100,
      })
      .eq('id', share.id);

    return share.id;
  } catch (err) {
    const errorMessage = err instanceof Error ? err.message : 'Unknown upload error';

    await supabase
      .from('external_video_shares')
      .update({
        upload_status: 'failed',
        upload_error: errorMessage,
      })
      .eq('id', share.id);

    throw err;
  }
}

