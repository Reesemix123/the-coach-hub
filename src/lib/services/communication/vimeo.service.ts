/**
 * Vimeo Service
 * Handles OAuth, account management, and video upload to Vimeo.
 *
 * Token storage note: access_token_vault_id stores the raw access token directly
 * in this implementation. In a production hardening pass, replace with Supabase
 * Vault secret IDs so tokens are encrypted at rest.
 */

import { createClient } from '@/utils/supabase/server';
import type { CoachExternalAccount, ExternalVideoShare, PrivacySetting } from '@/types/communication';

// ============================================================================
// Constants
// ============================================================================

const VIMEO_AUTH_URL = 'https://api.vimeo.com/oauth/authorize';
const VIMEO_TOKEN_URL = 'https://api.vimeo.com/oauth/access_token';
const VIMEO_API_BASE = 'https://api.vimeo.com';
const VIMEO_API_ACCEPT = 'application/vnd.vimeo.*+json;version=3.4';

// ============================================================================
// OAuth
// ============================================================================

/**
 * Build the Vimeo OAuth authorization URL to redirect the user to.
 * Requires VIMEO_CLIENT_ID and VIMEO_REDIRECT_URI environment variables.
 *
 * @param state - CSRF state token generated by the initiating route
 * @returns Full Vimeo authorization URL with query parameters
 */
export function getVimeoAuthUrl(state: string): string {
  const clientId = process.env.VIMEO_CLIENT_ID;
  const redirectUri = process.env.VIMEO_REDIRECT_URI;

  if (!clientId || !redirectUri) {
    throw new Error('Vimeo OAuth not configured (VIMEO_CLIENT_ID, VIMEO_REDIRECT_URI)');
  }

  const params = new URLSearchParams({
    response_type: 'code',
    client_id: clientId,
    redirect_uri: redirectUri,
    state,
    scope: 'upload video_files public private',
  });

  return `${VIMEO_AUTH_URL}?${params.toString()}`;
}

export interface VimeoTokenResult {
  accessToken: string;
  user: { uri: string; name: string };
  scope: string;
}

/**
 * Exchange a Vimeo authorization code for an access token.
 * Called once from the OAuth callback route after Vimeo redirects back.
 *
 * @param code - The authorization code from the Vimeo callback query string
 * @returns Access token, Vimeo user metadata, and granted scope string
 */
export async function exchangeCodeForToken(code: string): Promise<VimeoTokenResult> {
  const clientId = process.env.VIMEO_CLIENT_ID;
  const clientSecret = process.env.VIMEO_CLIENT_SECRET;
  const redirectUri = process.env.VIMEO_REDIRECT_URI;

  if (!clientId || !clientSecret || !redirectUri) {
    throw new Error('Vimeo OAuth credentials not configured (VIMEO_CLIENT_ID, VIMEO_CLIENT_SECRET, VIMEO_REDIRECT_URI)');
  }

  const credentials = Buffer.from(`${clientId}:${clientSecret}`).toString('base64');

  const response = await fetch(VIMEO_TOKEN_URL, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Accept': VIMEO_API_ACCEPT,
      'Authorization': `basic ${credentials}`,
    },
    body: JSON.stringify({
      grant_type: 'authorization_code',
      code,
      redirect_uri: redirectUri,
    }),
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`Vimeo token exchange failed (${response.status}): ${errorText}`);
  }

  const data = await response.json();

  return {
    accessToken: data.access_token as string,
    user: data.user as { uri: string; name: string },
    scope: data.scope as string,
  };
}

