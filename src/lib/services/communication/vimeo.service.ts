/**
 * Get all external video shares for a team, ordered newest-first.
 *
 * @param teamId - The team to query shares for
 */
export async function getExternalShares(teamId: string): Promise<ExternalVideoShare[]> {
  const supabase = await createClient();

  const { data, error } = await supabase
    .from('external_video_shares')
    .select('*')
    .eq('team_id', teamId)
    .order('created_at', { ascending: false });

  if (error) {
    throw new Error(`Failed to fetch external shares: ${error.message}`);
  }

  return data ?? [];
}

/**
 * Poll the Vimeo API for the current transcode status of an uploaded video.
 * Updates the external_video_shares record as the status progresses.
 * Returns the UploadStatus string for the caller to surface in the UI.
 *
 * @param shareId - The external_video_shares row ID
 */
export async function checkVimeoUploadStatus(shareId: string): Promise<string> {
  const supabase = await createClient();

  const { data: share } = await supabase
    .from('external_video_shares')
    .select('external_video_id, coach_id, upload_status')
    .eq('id', shareId)
    .single();

  if (!share) return 'pending';

  // Already in a terminal state — no need to poll Vimeo
  if (share.upload_status === 'complete' || share.upload_status === 'failed') {
    return share.upload_status;
  }

  if (!share.external_video_id) return share.upload_status ?? 'pending';

  const accessToken = await getVimeoAccessToken(share.coach_id);
  if (!accessToken) {
    return 'failed';
  }

  const response = await fetch(`${VIMEO_API_BASE}/videos/${share.external_video_id}`, {
    headers: {
      'Authorization': `bearer ${accessToken}`,
      'Accept': VIMEO_API_ACCEPT,
    },
  });

  if (!response.ok) {
    return 'failed';
  }

  const data = await response.json();
  const transcodeStatus: string | undefined = data.transcode?.status;

  if (transcodeStatus === 'complete') {
    await supabase
      .from('external_video_shares')
      .update({
        upload_status: 'complete',
        external_url: data.link ?? null,
        published_at: new Date().toISOString(),
      })
      .eq('id', shareId);

    return 'complete';
  }

  if (transcodeStatus === 'error') {
    await supabase
      .from('external_video_shares')
      .update({
        upload_status: 'failed',
        upload_error: 'Vimeo transcoding failed',
      })
      .eq('id', shareId);

    return 'failed';
  }

  // Still in progress
  return 'processing';
}
