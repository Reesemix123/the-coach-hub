/**
 * Persist a Vimeo connection in coach_external_accounts.
 * Upserts so that reconnecting an already-linked account refreshes credentials.
 *
 * @param coachId - Supabase auth user ID of the coach
 * @param accessToken - Raw Vimeo access token (stored in access_token_vault_id)
 * @param accountId - Vimeo user ID extracted from the user URI
 * @param accountName - Vimeo display name for the account
 */
export async function saveVimeoConnection(
  coachId: string,
  accessToken: string,
  accountId: string,
  accountName: string,
): Promise<void> {
  const supabase = await createClient();

  const { error } = await supabase
    .from('coach_external_accounts')
    .upsert(
      {
        coach_id: coachId,
        platform: 'vimeo',
        platform_account_id: accountId,
        platform_account_name: accountName,
        // TODO(hardening): replace raw token with a Supabase Vault secret ID
        access_token_vault_id: accessToken,
        status: 'active',
        connected_at: new Date().toISOString(),
      },
      { onConflict: 'coach_id,platform' },
    );

  if (error) {
    throw new Error(`Failed to save Vimeo connection: ${error.message}`);
  }
}

/**
 * Retrieve the coach's Vimeo account record, or null if not connected.
 *
 * @param coachId - Supabase auth user ID of the coach
 */
export async function getVimeoAccount(coachId: string): Promise<CoachExternalAccount | null> {
  const supabase = await createClient();

  const { data, error } = await supabase
    .from('coach_external_accounts')
    .select('*')
    .eq('coach_id', coachId)
    .eq('platform', 'vimeo')
    .single();

  if (error) {
    // PGRST116 = no rows found — not an error condition for this query
    if (error.code === 'PGRST116') return null;
    throw new Error(`Failed to fetch Vimeo account: ${error.message}`);
  }

  return data;
}

/**
 * Mark a Vimeo account as disconnected. Does not delete the record so that
 * historical external_video_shares retain their coach link.
 *
 * @param coachId - Supabase auth user ID of the coach
 */
export async function disconnectVimeo(coachId: string): Promise<void> {
  const supabase = await createClient();

  const { error } = await supabase
    .from('coach_external_accounts')
    .update({ status: 'disconnected' })
    .eq('coach_id', coachId)
    .eq('platform', 'vimeo');

  if (error) {
    throw new Error(`Failed to disconnect Vimeo: ${error.message}`);
  }
}

/**
 * Retrieve the raw Vimeo access token for an active coach connection.
 * Returns null if the account is missing, disconnected, or expired.
 *
 * @param coachId - Supabase auth user ID of the coach
 */
export async function getVimeoAccessToken(coachId: string): Promise<string | null> {
  const supabase = await createClient();

  const { data } = await supabase
    .from('coach_external_accounts')
    .select('access_token_vault_id, status')
    .eq('coach_id', coachId)
    .eq('platform', 'vimeo')
    .eq('status', 'active')
    .single();

  if (!data) return null;

  // TODO(hardening): retrieve from Supabase Vault using data.access_token_vault_id as the secret ID
  return data.access_token_vault_id;
}

